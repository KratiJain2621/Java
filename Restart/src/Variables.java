public class Variables 
{
	public static void main(String[] args)
	{

		System.out.println("Hello, Let's start learning Data Types\n\n1) Primitive Data Types \n");
		//System.out.println();
		char myLetter = 'I';
		boolean isPassed = true;
		byte marks = 127;
		int countInt = 12;
		float numberFloat = 3.12f;
		double piDouble = 3.14;
		short oneShort = 1235;
		long oneLong = 458721443485l;
		
		System.out.println("This is char data type --> "+myLetter);
		System.out.println("This is boolean data type --> "+isPassed);
		System.out.println("This is byte data type --> "+marks);
		System.out.println("This is int data type --> "+countInt);
		System.out.println("This is float data type --> "+numberFloat);
		System.out.println("This is double data type --> "+piDouble);
		System.out.println("This is short data type --> "+oneShort);
		System.out.println("This is long data type --> "+oneLong);
	}

}
